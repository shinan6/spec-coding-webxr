/**
 * @param {AssetEntry[]} manifest
 * @returns {string[]}
 */
export function validateManifest(manifest) {
  /** @type {string[]} */
  const errors = [];
  const seenNames = new Set();

  for (const entry of manifest) {
    if (!entry.fileName.endsWith(".glb")) {
      errors.push(`Invalid extension for ${entry.fileName}. Expected .glb`);
    }

    if (!entry.url.startsWith("https://")) {
      errors.push(`URL for ${entry.fileName} must use https`);
    }

    if (seenNames.has(entry.fileName)) {
      errors.push(`Duplicate filename in manifest: ${entry.fileName}`);
    }
    seenNames.add(entry.fileName);
  }

  return errors;
}

/**
 * @param {AssetEntry[]} manifest
 * @returns {string}
 */
export function buildAttributionMarkdown(manifest) {
  const lines = [
    "# Imported Vehicle Assets",
    "",
    "Generated by `npm run import-assets`.",
    "",
    "| File | Source | License | Notes |",
    "| --- | --- | --- | --- |"
  ];

  for (const entry of manifest) {
    lines.push(
      `| ${entry.fileName} | ${entry.source} | ${entry.license} | ${entry.notes} |`
    );
    lines.push(`| Source URL | ${entry.url} |  |  |`);
  }

  lines.push("");
  lines.push(
    "Review each model's upstream metadata for attribution requirements before redistribution."
  );

  return `${lines.join("\n")}\n`;
}
